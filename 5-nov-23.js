// JavaScript supports two types of comments: single-line and multi-line comments.

// 1. Single-Line Comments
// Syntax: Single-line comments start with //. Everything following // on that line is considered a comment and is ignored by the JavaScript engine.
// This is a single-line comment
let age = 30; // This is an inline comment explaining the variable

// 2. Multi-Line Comments
// Syntax: Multi-line comments start with /* and end with */. Everything between these markers is treated as a comment and is ignored by the JavaScript engine.

// Example:
// /*
// This is a multi-line comment.
// It can span multiple lines.
// Useful for longer explanations or commenting out blocks of code.
// */
let salary = 50000; // This is a single-line comment

